API_KEY = os.getenv("GOOGLE_BOOKS_API_KEY")

HOST     = os.getenv("POSTGRES_HOST")
USER     = os.getenv("POSTGRES_USER")
PASSWORD = os.getenv("POSTGRES_PASSWORD")
DB_NAME  = os.getenv("POSTGRES_DB")


CATEGORY_MAP = {
    "Fiction": "文学",
    "Literary Collections": "文学",
    "Philosophy": "哲学",
    "Business": "ビジネス",
    "Psychology": "心理学",
    "History": "歴史",
}


TITLE_BLACKLIST = ["対訳", "名文選", "トキメキ", "大活字"]

def search_book(title: str, genre: str = None):

    url = "https://www.googleapis.com/books/v1/volumes"
    params = {"q": title, "maxResults": 10, "key": API_KEY}

    response = requests.get(url, params=params)
    data = response.json()

    if not data.get("items"):
        print("本が見つかりませんでした")
        return


    item = next(
        (i for i in data["items"]
         if i.get("volumeInfo", {}).get("description")
         and len(i.get("volumeInfo", {}).get("authors", [])) == 1
         and not any(bad in i.get("volumeInfo", {}).get("title", "") for bad in TITLE_BLACKLIST)),
        data["items"][0]
    )
    info = item.get("volumeInfo", {})

    
    genre_name = genre if genre else _map_genre(info.get("categories", []))

    book = {
        "title":             info.get("title"),
        "author":            ", ".join(info.get("authors", [])),
        "ideology_keywords": info.get("categories", []),
        "author_message":    _extract_first_sentence(info.get("description", "")),
        "cover_image_url":   info.get("imageLinks", {}).get("thumbnail"),
        "preview_url":       info.get("previewLink"),
        "isbn":              _get_isbn(info),
        "genre_name":        genre_name,
    }

    print(f"取得: {book['title']} / {book['author']}")
    save_book(book)

def save_book(book: dict):
    conn = psycopg2.connect(host=HOST, user=USER, password=PASSWORD, dbname=DB_NAME)
    cur = conn.cursor()

    genre_id = None
    if book["genre_name"]:
        cur.execute("SELECT id FROM genres WHERE name = %s", (book["genre_name"],))
        row = cur.fetchone()
        if row:
            genre_id = row[0]

    
    cur.execute("""
        INSERT INTO books (title, author, genre_id, ideology_keywords, author_message, cover_image_url, preview_url, isbn)
        VALUES (%s, %s, %s, %s, %s, %s, %s, %s)
        ON CONFLICT (isbn) DO NOTHING
    """, (
        book["title"],
        book["author"],
        genre_id,
        book["ideology_keywords"],
        book["author_message"],
        book["cover_image_url"],
        book["preview_url"],
        book["isbn"] or None,
    ))

    conn.commit()
    print(f"保存完了: {book['title']}")

    cur.close()
    conn.close()

def _map_genre(categories: list) -> str:
    
    for category in categories:
        if category in CATEGORY_MAP:
            return CATEGORY_MAP[category]
    return ""

def _extract_first_sentence(text: str) -> str:
    
    if not text:
        return ""
    for sep in ["。", ".", "\n"]:
        idx = text.find(sep)
        if idx != -1:
            return text[:idx + 1]
    
    return text[:100]

def _get_isbn(info: dict) -> str:
    
    for identifier in info.get("industryIdentifiers", []):
        if identifier["type"] == "ISBN_13":
            return identifier["identifier"]
    return ""


