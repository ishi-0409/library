# ユーザーの入力に対して類語辞典のkeyをプラスする
def expand_input(user_input: str) -> str:
    expanded = user_input
    
    for canonical, synonyms in SYNONYMS.items():
        for synonym in synonyms:
            if synonym in user_input and canonical not in expanded:
                expanded += " " + canonical
    return expanded

